import numpy as np

arr = np.array([1,2,3])

# Sum of all elements of an array 
print(np.sum(arr))

# to Get Mean
print(np.mean(arr))

# Find the minimum elemets
print(np.min(arr))

# Find Maximum 
print(np.max(arr))

# Stander Division
print(np.std(arr))

# variance of elements in an array
print(np.var(arr))
